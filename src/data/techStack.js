// Get tech by id
export function getTechById(id) {
  return TECH_STACK.find(
    (t) => t.id === id || t.name.toLowerCase() === id?.toLowerCase(),
  );
}

// Get tech by name (fuzzy match)
export function getTechByName(name) {
  const normalized = name?.toLowerCase().replace(/[^a-z0-9]/g, "");
  return TECH_STACK.find(
    (t) =>
      t.name.toLowerCase().replace(/[^a-z0-9]/g, "") === normalized ||
      t.id === normalized,
  );
}
